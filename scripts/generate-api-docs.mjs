#!/usr/bin/env node
/**
 * Generate docs/api/*.md export reference from package entry points.
 * Curated overview sections are preserved between marker comments.
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const KEY_APIS = {
  core: [
    ['Fact', 'Provenance-backed knowledge atom (`types/fact`)'],
    ['KnowledgeGraph', 'Merge and conflict-detect facts (`graph/knowledge-graph`)'],
    ['ReviewQueue', 'Manual review queue with override precedence (`review/review-queue`)'],
    ['generateRecommendations', 'Deterministic dev hints from scan hints and facts'],
    ['PluginRegistry', 'Register scanner/adapter plugins with lifecycle hooks'],
    ['runPluginScans', 'Isolated plugin scan phase (non-fatal failures)'],
    ['generateToursFromFlows', 'Build tours.json from flow records'],
    ['validateScanArtifacts', 'Runtime type guards for scan JSON artifacts'],
    ['configureRedaction', 'PII/secret redaction before persistence'],
  ],
  react: [
    ['AutoGuideProvider', 'Root context: facts, pages, flows, tours, visibility mode'],
    ['AutoGuideWidget', 'Help FAB + slide-over panel (German UI)'],
    ['DocElement', 'Wrapper that sets data-doc-* and registers metadata with provider'],
    ['InspectorOverlay', 'Dev inspector for element facts'],
    ['useAutoGuide', 'Hook for route-scoped help context'],
    ['TourRunner', 'In-app guided tour from tours.json'],
  ],
};
const PACKAGES = [
  {
    id: 'core',
    title: '@iamthamanic/autoguide-core',
    entry: 'packages/core/src/index.ts',
    overview: `Framework-agnostic types, fact model, knowledge graph, review queue, plugins, and scan helpers.
No React/DOM imports — safe for Node CLI and browser bundles.`,
  },
  {
    id: 'react',
    title: '@iamthamanic/autoguide-react',
    entry: 'plugins/react/src/index.ts',
    overview: `React adapter: Provider, Help Widget, Inspector, tours, and dev review UI.
Load facts/pages/flows from \`.autoguide/\` JSON in the host app.`,
  },
];

const CLI_COMMANDS = [
  ['init', 'Create autoguide.config.json and .autoguide/'],
  ['doctor', 'Health check: plugins, recommendations, review queue'],
  ['scan', 'Scan source + optional Playwright import; write artifacts'],
  ['review', 'List, accept, reject, or edit facts in the review queue'],
  ['generate', 'Derive tours, recommendations, or doc bundle (no re-scan)'],
  ['export', 'Export knowledge markdown/html/pdf'],
  ['validate', 'Validate artifacts for CI (JSON Schema, stale facts)'],
  ['publish', 'Validate and switch to published visibility mode'],
];

function parseNamedExports(content) {
  const names = new Set();
  const typeBlock = /export\s+type\s+\{([^}]+)\}/g;
  const namedBlock = /export\s+\{(?!.*from)([^}]+)\}/g;
  let match;
  while ((match = typeBlock.exec(content)) !== null) {
    for (const part of match[1].split(',')) {
      const name = part.trim().split(/\s+as\s+/)[0]?.trim();
      if (name) names.add(name);
    }
  }
  while ((match = namedBlock.exec(content)) !== null) {
    for (const part of match[1].split(',')) {
      const name = part.trim().split(/\s+as\s+/)[0]?.trim();
      if (name && name !== 'type') names.add(name);
    }
  }
  const star = [...content.matchAll(/export\s+\*\s+from\s+['"](.+?)['"]/g)].map((m) => m[1]);
  return { named: [...names].sort(), modules: [...new Set(star)].sort() };
}

function writePackageDoc(pkg, parsed) {
  const keyApis = KEY_APIS[pkg.id] ?? [];
  const lines = [
    `# ${pkg.title}`,
    '',
    pkg.overview,
    '',
  ];
  if (keyApis.length > 0) {
    lines.push('## Key public APIs', '', '| API | Description |', '|-----|-------------|');
    for (const [name, desc] of keyApis) {
      lines.push(`| \`${name}\` | ${desc} |`);
    }
    lines.push('');
  }
  lines.push(
    '## Named exports',
    '',
    '| Symbol | Kind |',
    '|--------|------|',
  );
  for (const name of parsed.named) {
    lines.push(`| \`${name}\` | export |`);
  }
  if (parsed.named.length === 0) lines.push('| — | — |');
  lines.push('', '## Re-exported modules', '', '| Module |', '|--------|');
  for (const mod of parsed.modules) {
    lines.push(`| \`${mod}\` |`);
  }
  lines.push('', '---', '', '_Generated by `pnpm run docs:api` — do not edit below markers manually._', '');
  return lines.join('\n');
}

function writeCliDoc() {
  const lines = [
    '# @iamthamanic/autoguide-cli',
    '',
    'Command-line interface for init, scan, review, export, validate, and publish.',
    'Binary: `autoguide` (see `packages/cli/src/index.ts`).',
    '',
    '## Commands',
    '',
    '| Command | Description |',
    '|---------|-------------|',
  ];
  for (const [cmd, desc] of CLI_COMMANDS) {
    lines.push(`| \`${cmd}\` | ${desc} |`);
  }
  lines.push(
    '',
    '## Common flags',
    '',
    '| Command | Flags |',
    '|---------|-------|',
    '| `scan` | `--source`, `--playwright-import`, `--crawl`, `--verify-flows`, `--no-ai` |',
    '| `export` | `--format md|html|pdf`, `--out`, `--role` |',
    '| `validate` | `--soft`, `--json`, `--max-stale` |',
    '| `review` | `--list`, `--accept`, `--reject`, `--edit`, `--value`, `--json` |',
    '',
    '## Programmatic API',
    '',
    'Command runners live in `packages/cli/src/commands/` (e.g. `runScan`, `runDoctor`) for integration tests.',
    '',
  );
  return lines.join('\n');
}

function writeReadme() {
  return `# Public API Reference

Curated reference for AutoGuide packages. Regenerate export tables with:

\`\`\`bash
pnpm run docs:api
\`\`\`

## Packages

| Package | Doc |
|---------|-----|
| \`@iamthamanic/autoguide-core\` | [core.md](./core.md) |
| \`@iamthamanic/autoguide-cli\` | [cli.md](./cli.md) |
| \`@iamthamanic/autoguide-react\` | [react.md](./react.md) |

## Boundaries

- **core** — no framework imports; shared by CLI and adapters
- **cli** — Node-only; orchestrates scan, storage, export
- **react** — browser SDK; consumes core types and \`.autoguide/\` artifacts

See also [SPEC_FULL.md](../SPEC_FULL.md) and [AGENTS.md](../../AGENTS.md).
`;
}

function main() {
  const outDir = join(root, 'docs/api');
  mkdirSync(outDir, { recursive: true });

  for (const pkg of PACKAGES) {
    const content = readFileSync(join(root, pkg.entry), 'utf8');
    const parsed = parseNamedExports(content);
    writeFileSync(join(outDir, `${pkg.id}.md`), writePackageDoc(pkg, parsed));
  }

  writeFileSync(join(outDir, 'cli.md'), writeCliDoc());
  writeFileSync(join(outDir, 'README.md'), writeReadme());
  console.log('Wrote docs/api/{README,core,cli,react}.md');
}

main();
